int	ft_strcmp(char *s1, char *s2)
{
	int	kount;

	if (s1 && s2)
	{
		kount = 0;
		while (s1[kount] != '\0' || s2[kount] != '\0')
		{
			if (s1[kount] != s2[kount])
				return (s1[kount] - s2[kount]);
			kount++;
		}
	}
	return (0);
}

